package Practice;

public class Op {
    public static void main(String[] args) {
        int x=10;
        System.out.println(x);
        System.out.println(x++);
        System.out.println(++x);
        System.out.println(x--);
        System.out.println(--x);
        System.out.println();

        int a=10;
        int b=10;

        System.out.println(a++ + ++a);
        System.out.println(b++ + b++);

    }
}
